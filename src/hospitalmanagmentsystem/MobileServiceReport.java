   public void getTotal(){
        int sum = 0;

        for(int i=0; i<jTable1.getRowCount(); i++)
        {
            sum = sum + Integer.parseInt(jTable1.getValueAt(i, 2).toString());
        }
        totBookings.setText(Integer.toString(sum));
    }
   
   public void getMax()
    {
        int max = 0;
        for(int i=0; i<jTable1.getRowCount(); i++)
        {
           int val = Integer.parseInt(jTable1.getValueAt(i, 2).toString());
           if(val > max)
           {
               max = val;
           }
        }
        noOfBookings.setText(Integer.toString(max));
    }
   
   public void getMin() 
   {
        int min = 0;

        for (int i = 0; i < jTable1.getRowCount(); i++) 
        {
            if (i == 0) 
            {
                min = Integer.parseInt(jTable1.getValueAt(i, 2).toString());
            } 
            else 
            {
                int val = Integer.parseInt(jTable1.getValueAt(i, 2).toString());
                if (val < min) {
                min = val;
            }
        }
        }
        minNoOfBookings.setText(Integer.toString(min));
    }
   
