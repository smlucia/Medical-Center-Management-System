    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        int serviceDate = jMonthChooser1.getMonth()+1;
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con1=DriverManager.getConnection("jdbc:mysql://localhost:3308/channelingcenter","root","");
            insert=con1.prepareStatement("SELECT count(mobileservice.packageId) as 'total',servicepackage.serviceName,servicepackage.PackageFee FROM mobileservice, servicepackage WHERE servicepackage.packageId = mobileservice.packageId AND month(mobileservice.serDate) = ? GROUP BY servicepackage.serviceName,servicepackage.PackageFee");
            insert.setInt(1, serviceDate);

            //selectAddresult.setString(1, Specialty.getSelectedItem().toString());
            ResultSet rs=insert.executeQuery();

            ResultSetMetaData res =rs.getMetaData();

            int c= res.getColumnCount();

            DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
            df.setRowCount(0);

            while(rs.next()){
                Vector v2 = new Vector ();
                for (int a=1;a<=c;a++){
                    v2.add(rs.getString("serviceName"));
                    v2.add(rs.getString("PackageFee"));
                    v2.add(rs.getString("total"));
                }
                df.addRow(v2);
            }

        } catch (SQLException ex) {
            Logger.getLogger(MobileServiceReport.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(MobileServiceReport.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

